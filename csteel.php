<p>Thermo Mechanically Treated Bars "TMT" replaced CTD bars in the late 1990's as they were considered technically superior and it was convenient for the rolling mills. Cold twisting post production was a cumbersome and labour intensive job and often proved a bottleneck in production of CTD bars.</p>

<p>The use of progressively higher strength reinforcement steel resulted in increased demand for smaller diameter steel bars, to replace higher diameter steel bars, due to economic reasons .Production of CTD bars or TMT bars was limited to 8mm dia.</p>

<p>The solution was found by invention of a new type of reinforcement bar ,the high strength deformed bar. Because of their economic as well as technical excellence these bars gained international acceptance easily.</p>

<p>TRICON<sup>TM</sup> high strength deformed bars conforming to IS: 1786 (1985) of Grade Fe550 corresponding to DIN: 488 &amp; B.S. 4483 are manufactured using the well-established technology of simultaneous cold rolling and cold ribbing.</p>

<p>TRICON<sup>TM</sup> bars can be used as a general-purpose concrete reinforcement in all types of reinforced concrete structures. It can be used in combination with any grade of concrete like M-15, M-20 &amp; M-25 with economy, better functional aspects, structural safety and strength.
</p>
